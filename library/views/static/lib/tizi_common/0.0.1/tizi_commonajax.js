define(function(require,exports){
	//反馈提交表单结果
	exports.feedbacksubmit = function(data){
		if(data.errorcode){
			$.tiziDialog({
				content:data.error,
				time:3,
				lock:true,
				icon:'succeed'
			});
			$(".feedbackForm").Validform().resetForm();
		}else{
			$.tiziDialog({
				content:data.error,
				time:3,
				lock:true,
				icon:'error'
			})
		}	
	}
});